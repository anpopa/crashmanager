#include "cdh-info.h"

#include <errno.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <sys/queue.h>
#include <sys/stat.h>
#include <sys/types.h>
#include <unistd.h>

CdhInfo *
cdh_info_new (void)
{
  CdhInfo *i = calloc (1, sizeof(CdhInfo));

  g_assert (i);
  i->onhost = (guint8)true;

  return i;
}

void
cdh_info_free (CdhInfo *i)
{
  g_assert (i);
  free (i);
}
